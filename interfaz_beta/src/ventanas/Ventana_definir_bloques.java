    private void campo_bloqueKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_campo_bloqueKeyReleased
        //campo_tiempo.setText(campo_bloque.getText());

        if(campo_bloque.getText().length()>0){
            int numeroBloques = Integer.parseInt(campo_bloque.getText());
            if(numeroBloques ==0){
                campo_tiempo.setText(calcularRangoHora(0)); 
            }else{
                float div = 1440/numeroBloques;
                String rangoTiempo = calcularRangoHora(div);
                campo_tiempo.setText(rangoTiempo);   
            }
        }else{
            campo_tiempo.setText(calcularRangoHora(0)); 
        }
        
        
    }//GEN-LAST:event_campo_bloqueKeyReleased

